#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int	i;
	int slen;

	if (!s)
		return ;
	i = 0;
	slen = ft_strlen(s);
	while (i < slen)
	{
		f(&s[i]);
		i++;
	}
}
