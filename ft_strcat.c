#include "libft.h"

STR	ft_strcat(STR s1, const STR s2)
{
	int i;
	int s1len;

	i = 0;
	s1len = ft_strlen(s1);
	while (s2[i] != '\0')
	{
		s1[s1len + i] = s2[i];
		i++;
	}
	s1[s1len + i] = '\0';
	return (s1);
}
