#include "libft.h"

MAXUNBR	ft_parrlen(void **arr)
{
	MAXUNBR	i;
	char	**arr_c;

	i = 0;
	arr_c = (char **)arr;
	while (arr_c[i])
		i++;
	return (i);
}
