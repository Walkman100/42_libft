#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*s_;
	size_t			i;

	s_ = s;
	i = 0;
	while (i < n)
	{
		s_[i] = c;
		i++;
	}
	return (s);
}
