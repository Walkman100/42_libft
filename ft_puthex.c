#include "libft.h"

void		ft_puthex(MAXUNBR n)
{
	if (n > 15)
	{
		ft_puthex(n / 16);
		n %= 16;
	}
	if (n < 10)
		ft_putchar(n + '0');
	else
		ft_putchar(n + 87);
}
